package com.ccube9.gochat.Util;

public class ParticipatedList {
    String id;
    String profile_image;
    String last_name;
    String first_name;
    String Type_of_currency;
    String amount;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getProfile_image() {
        return profile_image;
    }

    public void setProfile_image(String profile_image) {
        this.profile_image = profile_image;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getType_of_currency() {
        return Type_of_currency;
    }

    public void setType_of_currency(String type_of_currency) {
        Type_of_currency = type_of_currency;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }
}
