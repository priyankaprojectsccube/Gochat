package com.ccube9.gochat.Util;

public class STORY {
    String story_details_id;
    String first_name;
    String last_name;
    String profile_image;
    String flag_story;
    String story_user_id;

    public String getStory_details_id() {
        return story_details_id;
    }

    public void setStory_details_id(String story_details_id) {
        this.story_details_id = story_details_id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getProfile_image() {
        return profile_image;
    }

    public void setProfile_image(String profile_image) {
        this.profile_image = profile_image;
    }

    public String getFlag_story() {
        return flag_story;
    }

    public void setFlag_story(String flag_story) {
        this.flag_story = flag_story;
    }

    public String getStory_user_id() {
        return story_user_id;
    }

    public void setStory_user_id(String story_user_id) {
        this.story_user_id = story_user_id;
    }
}
